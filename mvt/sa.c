#include "push_swap.h"

void    sa(t_tab *a)
{
    int tmp;

    if (!a || a->top < 1)
        return ;
    tmp = a->tab[a->top];
    a->tab[a->top] = a->tab[a->top - 1];
    a->tab[a->top - 1] = tmp;
    ft_printf("sa\n");
}
